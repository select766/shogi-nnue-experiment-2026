"""
Dataset for Expert Blending model training.

Reads packed SFEN .bin files (40 bytes/record) and generates both
NNUE sparse features and dlshogi dense features for the same positions.

NNUE features are generated by the C++ SparseBatchProvider (same code path
as baseline NNUE training) to ensure feature consistency.
dlshogi features are generated by converting PackedSfen (YaneuraOu format)
to HCP (Apery format) via cshogi, then using dcppshogi.hcpe_decode_with_value.

Note: The .bin files use YaneuraOu's PackedSfen encoding, which is incompatible
with dlshogi's HuffmanCodedPos (Apery) encoding despite both being 32 bytes.
Direct use of PackedSfen bytes as HCP causes "incorrect Huffman code" errors.

Usage:
    cd nnue-pytorch && source .venv/bin/activate
    PYTHONPATH=../src:$PYTHONPATH python -m train_nnue.expert_blending_dataset \
        --bin data/val.bin --feature-set "HalfKP" --batch-size 256
"""

import mmap
import os
import time

import cshogi
import numpy as np
import torch
from torch.utils.data import DataLoader, Dataset, IterableDataset

import dlshogi.cppshogi as dcppshogi
from dlshogi.common import FEATURES1_NUM, FEATURES2_NUM

import nnue_dataset
import features as nnue_features

# Packed SFEN record layout (40 bytes, YaneuraOu PackedSfenValue format)
RECORD_BYTES = 40
# offset 0: PackedSfen (32 bytes), offset 32: score (int16), offset 34: move (uint16),
# offset 36: gamePly (uint16), offset 38: game_result (int8), offset 39: padding (uint8)

# HCPE dtype for cppshogi.hcpe_decode_with_value (Apery HuffmanCodedPosAndEval format)
HCPE_DTYPE = np.dtype([
    ('hcp', np.uint8, 32),
    ('eval', np.int16),
    ('bestMove16', np.uint16),
    ('gameResult', np.uint8),
    ('padding', np.uint8),
])


class ExpertBlendingDataset(IterableDataset):
    """Packed SFEN .bin から NNUE + DNN 両方の特徴量をバッチ生成する。

    NNUE sparse features: C++ SparseBatchProvider 経由（ベースライン NNUE 学習と同じコードパス）
    dlshogi dense features: PackedSfen → HCP 変換後 dcppshogi.hcpe_decode_with_value 経由

    両者は同じファイルを同じ順序で逐次読みするため、同じ局面を参照する。
    """

    def __init__(self, bin_path, feature_set_name, batch_size, device='cpu', shuffle=True):
        super().__init__()
        self.bin_path = bin_path
        self.feature_set_name = feature_set_name
        self.batch_size = batch_size
        self.device = device
        # shuffle パラメータは互換性のため残すが、ランダム化は cyclic 読み込みで実現
        self.shuffle = shuffle
        self.num_records = os.path.getsize(bin_path) // RECORD_BYTES
        self.feature_set = nnue_features.get_feature_set_from_name(feature_set_name)

    def __iter__(self):
        return _ExpertBlendingIterator(self)


class _ExpertBlendingIterator:
    """Stateful iterator for ExpertBlendingDataset.

    2つの並行リーダーで同じファイルを逐次読みする:
    1. SparseBatchProvider (C++): NNUE sparse features (us, them, white, black, outcome, score, ply)
    2. Python mmap reader: PackedSfen → cshogi変換 → HCP → dlshogi dense features (x1, x2)

    .binファイルはYaneuraOuのPackedSfen形式だが、dlshogiはAperyのHCP形式を期待する。
    cshogi.Board.set_psfen() + to_hcp() で形式変換してから特徴量生成を行う。
    両リーダーは同じバッチサイズで同じ順序に読むため同期が保たれる。
    """

    def __init__(self, dataset):
        self.batch_size = dataset.batch_size
        self.device = dataset.device
        self.num_records = dataset.num_records

        # NNUE features: C++ SparseBatchProvider (correct feature generation)
        # cyclic=True: ファイル末尾で先頭に戻る (FixedNumBatchesDataset と組み合わせて使う)
        # random_fen_skipping=0, filtered=False: レコードをスキップしない (Python側と同期)
        self.nnue_provider = nnue_dataset.SparseBatchProvider(
            dataset.feature_set_name,
            dataset.bin_path,
            dataset.batch_size,
            cyclic=True,
            num_workers=1,
            filtered=False,
            random_fen_skipping=0,
            device=dataset.device,
        )

        # dlshogi features: PackedSfen bytes を同じファイルから逐次読み → HCP変換
        self.file = open(dataset.bin_path, 'rb')
        self.mm = mmap.mmap(self.file.fileno(), 0, access=mmap.ACCESS_READ)
        self.record_pos = 0
        self.board = cshogi.Board()  # PackedSfen → HCP 変換用

    def __iter__(self):
        return self

    def __next__(self):
        # 1. NNUE sparse features from C++ reader
        nnue_tensors = next(self.nnue_provider)
        us, them, white, black, outcome, score, ply = nnue_tensors
        actual_batch_size = us.shape[0]

        # 2. Read PackedSfen bytes and convert to HCP (Apery format) for dlshogi
        # .bin files use YaneuraOu PackedSfen encoding; dlshogi expects Apery HCP encoding.
        # Convert via cshogi: set_psfen(PackedSfen) → to_hcp(HCP)
        hcps = np.zeros((actual_batch_size, 32), dtype=np.uint8)
        hcp_tmp = np.zeros(1, dtype=cshogi.dtypeHcp)  # shape (1, 32), uint8
        psfen_tmp = np.zeros(1, dtype=cshogi.PackedSfen)  # structured: [('sfen', 'u1', (32,))]
        for i in range(actual_batch_size):
            if self.record_pos >= self.num_records:
                self.record_pos = 0  # wrap around (cyclic, matching SparseBatchProvider)
            offset = self.record_pos * RECORD_BYTES
            psfen_tmp[0]['sfen'] = np.frombuffer(self.mm[offset:offset + 32], dtype=np.uint8)
            self.board.set_psfen(psfen_tmp)
            self.board.to_hcp(hcp_tmp)
            hcps[i] = hcp_tmp[0]
            self.record_pos += 1

        # 3. Generate dlshogi dense features from HCP (now in Apery format)
        hcpe_batch = np.zeros(actual_batch_size, dtype=HCPE_DTYPE)
        hcpe_batch['hcp'] = hcps

        features1 = np.zeros((actual_batch_size, FEATURES1_NUM, 9, 9), dtype=np.float32)
        features2 = np.zeros((actual_batch_size, FEATURES2_NUM, 9, 9), dtype=np.float32)
        move_dummy = np.zeros(actual_batch_size, dtype=np.int64)
        result_dummy = np.zeros(actual_batch_size, dtype=np.float32)
        value_dummy = np.zeros(actual_batch_size, dtype=np.float32)

        dcppshogi.hcpe_decode_with_value(
            hcpe_batch.view(np.uint8).reshape(actual_batch_size, -1),
            features1, features2, move_dummy, result_dummy, value_dummy,
        )

        x1 = torch.from_numpy(features1).to(device=self.device)
        x2 = torch.from_numpy(features2).to(device=self.device)

        return x1, x2, us, them, white, black, outcome, score, ply

    def _close(self):
        if hasattr(self, 'mm') and self.mm is not None:
            self.mm.close()
            self.mm = None
        if hasattr(self, 'file') and self.file is not None:
            self.file.close()
            self.file = None

    def __del__(self):
        self._close()


class FixedNumBatchesDataset(Dataset):
    """Wraps an IterableDataset to yield a fixed number of batches per epoch."""

    def __init__(self, dataset, num_batches):
        super().__init__()
        self.dataset = dataset
        self.iter = iter(self.dataset)
        self.num_batches = num_batches

    def __len__(self):
        return self.num_batches

    def __getitem__(self, idx):
        return next(self.iter)


def create_data_loaders(train_bin, val_bin, feature_set_name, batch_size, device, epoch_size):
    """学習/検証用DataLoaderのペアを返す。

    Args:
        train_bin: 学習用 packed SFEN .bin パス
        val_bin: 検証用 packed SFEN .bin パス
        feature_set_name: NNUE特徴セット名 (例: "HalfKP")
        batch_size: バッチサイズ
        device: デバイス文字列 (例: "cuda:0")
        epoch_size: 1エポックあたりの局面数

    Returns:
        (train_loader, val_loader) のタプル
    """
    train_dataset = ExpertBlendingDataset(
        train_bin, feature_set_name, batch_size, device=device, shuffle=True
    )
    val_dataset = ExpertBlendingDataset(
        val_bin, feature_set_name, batch_size, device=device, shuffle=False
    )

    num_train_batches = (epoch_size + batch_size - 1) // batch_size
    val_records = os.path.getsize(val_bin) // RECORD_BYTES
    num_val_batches = (min(val_records, 1_000_000) + batch_size - 1) // batch_size

    train_loader = DataLoader(
        FixedNumBatchesDataset(train_dataset, num_train_batches),
        batch_size=None,
        batch_sampler=None,
    )
    val_loader = DataLoader(
        FixedNumBatchesDataset(val_dataset, num_val_batches),
        batch_size=None,
        batch_sampler=None,
    )
    return train_loader, val_loader


